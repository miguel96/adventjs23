export function drawClock(time: string): string[][] {
  const symbols = {
    ":": [[" ", " ", "*", " ", "*", " ", " "]],
    "0": [
      "*".repeat(7).split(""),
      ["*", ..." ".repeat(5).split(""), "*"],
      "*".repeat(7).split(""),
    ],
    "1": [
      " ".repeat(7).split(""),
      " ".repeat(7).split(""),
      "*".repeat(7).split(""),
    ],
    "2": [
      ["*", " ", " ", "*", "*", "*", "*"],
      ["*", " ", " ", "*", " ", " ", "*"],
      ["*", "*", "*", "*", " ", " ", "*"],
    ],
    "3": [
      ["*", " ", " ", "*", " ", " ", "*"],
      ["*", " ", " ", "*", " ", " ", "*"],
      "*".repeat(7).split(""),
    ],
    "4": [
      ["*", "*", "*", "*", " ", " ", " "],
      [" ", " ", " ", "*", " ", " ", " "],
      ["*", "*", "*", "*", "*", "*", "*"],
    ],
    "5": [
      ["*", "*", "*", "*", " ", " ", "*"],
      ["*", " ", " ", "*", " ", " ", "*"],
      ["*", " ", " ", "*", "*", "*", "*"],
    ],
    "6": [
      ["*", "*", "*", "*", "*", "*", "*"],
      ["*", " ", " ", "*", " ", " ", "*"],
      ["*", " ", " ", "*", "*", "*", "*"],
    ],
    "7": [
      ["*", " ", " ", " ", " ", " ", " "],
      ["*", " ", " ", " ", " ", " ", " "],
      ["*", "*", "*", "*", "*", "*", "*"],
    ],
    "8": [
      ["*", "*", "*", "*", "*", "*", "*"],
      ["*", " ", " ", "*", " ", " ", "*"],
      ["*", "*", "*", "*", "*", "*", "*"],
    ],
    "9": [
      ["*", "*", "*", "*", " ", " ", "*"],
      ["*", " ", " ", "*", " ", " ", "*"],
      ["*", "*", "*", "*", "*", "*", "*"],
    ],
  };
  const resp: string[][] = [];

  time.split("").forEach((el, index) => {
    // @ts-expect-error test
    const elements = el in symbols ? symbols[el] : [" ".repeat(7).split("")];
    resp.push(
      ...elements,
      ...(index < time.split("").length - 1 ? [" ".repeat(7).split("")] : []),
    );
  });
  const clock = [];
  let i = 0;
  while (i < 7) {
    clock.push(resp.map((el) => el[i]));
    i += 1;
  }
  console.log(
    clock
      .map((el) => el.map((el) => (el === " " ? "_" : el)).join(""))
      .join("\n")
      .replace(/_/g, " "),
  );
  return clock;
}
